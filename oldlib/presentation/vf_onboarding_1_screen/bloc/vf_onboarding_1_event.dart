part of 'vf_onboarding_1_bloc.dart';

/// Abstract class for all events that can be dispatched from the
///VfOnboarding1 widget.
///
/// Events must be immutable and implement the [Equatable] interface.
class VfOnboarding1Event extends Equatable {
  @override
  List<Object?> get props => [];
}

/// Event that is dispatched when the VfOnboarding1 widget is first created.
class VfOnboarding1InitialEvent extends VfOnboarding1Event {
  @override
  List<Object?> get props => [];
}
