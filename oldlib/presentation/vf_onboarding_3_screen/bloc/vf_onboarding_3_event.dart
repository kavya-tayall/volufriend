part of 'vf_onboarding_3_bloc.dart';

/// Abstract class for all events that can be dispatched from the
///VfOnboarding3 widget.
///
/// Events must be immutable and implement the [Equatable] interface.
class VfOnboarding3Event extends Equatable {
  @override
  List<Object?> get props => [];
}

/// Event that is dispatched when the VfOnboarding3 widget is first created.
class VfOnboarding3InitialEvent extends VfOnboarding3Event {
  @override
  List<Object?> get props => [];
}
